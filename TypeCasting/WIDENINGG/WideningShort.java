class WideningShort {
	public static void main(String[] args) {

	   //SHORT INTO INTEGER
		short num1=20;
		int num2=num1+1;
		System.out.println(num1);  //20
		System.out.println(num2);  //21

	   //SHORT INTO LONG
		short num3=22;
		long num4=num3+1;
		System.out.println(num3);  //22
		System.out.println(num4);  //23

	   //SHORT INTO FLOAT
		short num5=24;
		float num6=num5+1;
		System.out.println(num5);  //24
		System.out.println(num6);  //25
		
	   //SHORT INTO DOUBLE
		short num7=26;
		double num8=num7+1;
		System.out.println(num7);  //26
		System.out.println(num8);  //27

	   //SHORT INTO CHAR
		short num9=65;
		char num10=(char)num9;
		System.out.println(num9);  //65
		System.out.println(num10);  //A
	}
}