class WideningInteger {
	public static void main(String[] args) {
		
	   //INTEGER INTO LONG
		int num1=30;
		long num2=num1+1;
		System.out.println(num1);   //30
		System.out.println(num2+"\n");   //31

	   //INTEGER INTO FLOAT
		int num3=33;
		float num4=num3+1;
		System.out.println(num3);   //33
		System.out.println(num4+"\n");   //34

	   //INTEGER INTO DOUBLE
		int num5=35;
		double num6=num5+1;
		System.out.println(num5);   //35
		System.out.println(num6+"\n");   //36

	   //INTEGER INTO CHAR
		int num7=98;
		char num8=(char)(num7+1);
		System.out.println(num7);   //98
		System.out.println(num8+"\n");   //c

	}
}