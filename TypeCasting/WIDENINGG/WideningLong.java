class WideningLong {
	public static void main(String[] args) {

	   //LONG INTO FLOAT
		long num1=40;
		float num2=num1+1;
		System.out.println(num1);   //40
		System.out.println(num2+"\n");   //41.0

	   //LONG INTO DOUBLE
		long num3=42;
		double num4=num3+1;
		System.out.println(num3);   //42
		System.out.println(num4+"\n");   //43.0

	   //LONG INTO CHAR
		long num5=49;
		char num6=(char)(num5+1);
		System.out.println(num5);   //44
		System.out.println(num6+"\n");   //1

	}
}