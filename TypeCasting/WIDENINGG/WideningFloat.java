class WideningFloat {
	public static void main(String[] args) {

	   //FLOAT INTO DOUBLE
		float num3=40;
		double num4=num3+1;
		System.out.println(num3);   //40
		System.out.println(num4+"\n");   //41

	   //FLOAT INTO CHAR
		float num5=49;
		char num6=(char) (num5+1);
		System.out.println(num5);   //49
		System.out.println(num6+"\n");   //2

	}
}